# Este programa irá calcular a área de um triângulo. Peça para a pessoa informar a medida 
# numérica da base do triângulo, depois colete o valor da altura.  Apresente o valor da área do triângulo.

base =  float(input('Digite um valor para a base do triângulo: '))
altura = float(input('Digite um valor para a altura do triângulo: '))

area = (base * altura) / 2
print(f'A área do triângulo é de: {area} cm')